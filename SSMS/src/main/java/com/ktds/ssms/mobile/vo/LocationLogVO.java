package com.ktds.ssms.mobile.vo;

public class LocationLogVO {

	private String locationLogId;
	private String memberId;
	private String locationId;
	private String time;
	
	public String getLocationLogId() {
		return locationLogId;
	}
	public void setLocationLogId(String locationLogId) {
		this.locationLogId = locationLogId;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getLocationId() {
		return locationId;
	}
	public void setLocationId(String locationId) {
		this.locationId = locationId;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}
